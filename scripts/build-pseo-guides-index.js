#!/usr/bin/env node
/**
 * Generate lib/data/pseo-guides/index.ts — a statically-imported map of every
 * <slug>.json pSEO guide sidecar, mirroring build-treatment-content-index.js.
 * pseo-content.ts re-exports this map as `procedureGuides`; runtime fs reads
 * are not bundled into the Netlify serverless function, hence static imports.
 * Re-run after adding guide sidecars.
 */
const fs = require('fs');
const path = require('path');

const dir = path.join(__dirname, '..', 'lib', 'data', 'pseo-guides');
const files = fs.readdirSync(dir).filter((f) => f.endsWith('.json') && !f.startsWith('_'));
const camel = (s) => s.replace(/[^a-zA-Z0-9]+(.)/g, (_, c) => c.toUpperCase()).replace(/^[0-9]/, '_$&');

let out = '// AUTO-GENERATED by scripts/build-pseo-guides-index.js — do not edit by hand.\n';
out += "import type { ProcedureGuide } from '@/lib/data/pseo-content'\n\n";
const vars = [];
for (const f of files) {
  const slug = f.replace(/\.json$/, '');
  const v = camel(slug);
  vars.push([slug, v]);
  out += `import ${v} from './${f}'\n`;
}
out += '\nexport const pseoGuides: Record<string, ProcedureGuide> = {\n';
// `as unknown as` — JSON module inference produces never[] for empty arrays,
// which TS2352-fails a direct cast (same issue as treatment-content).
for (const [slug, v] of vars) out += `  ${JSON.stringify(slug)}: ${v} as unknown as ProcedureGuide,\n`;
out += '}\n';

fs.writeFileSync(path.join(dir, 'index.ts'), out);
console.log(`wrote lib/data/pseo-guides/index.ts with ${files.length} guides`);
