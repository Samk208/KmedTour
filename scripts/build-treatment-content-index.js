#!/usr/bin/env node
/**
 * Generate lib/data/treatment-content/index.ts — a statically-imported map of
 * every <slug>.json sidecar. The procedure page imports this map instead of
 * reading the JSON with fs at request time: runtime fs reads with
 * process.cwd()-relative paths are NOT bundled into the Netlify serverless
 * function and fail in production. Re-run after adding sidecars (slice 2+).
 */
const fs = require('fs');
const path = require('path');

const dir = path.join(__dirname, '..', 'lib', 'data', 'treatment-content');
const files = fs.readdirSync(dir).filter((f) => f.endsWith('.json') && !f.startsWith('_'));
const camel = (s) => s.replace(/[^a-zA-Z0-9]+(.)/g, (_, c) => c.toUpperCase()).replace(/^[0-9]/, '_$&');

let out = '// AUTO-GENERATED by scripts/build-treatment-content-index.js — do not edit by hand.\n';
out += "import type { TreatmentRichContent } from '@/components/treatments/treatment-rich-sections'\n\n";
const vars = [];
for (const f of files) {
  const slug = f.replace(/\.json$/, '');
  const v = camel(slug);
  vars.push([slug, v]);
  out += `import ${v} from './${f}'\n`;
}
out += '\nexport const treatmentContent: Record<string, TreatmentRichContent> = {\n';
for (const [slug, v] of vars) out += `  ${JSON.stringify(slug)}: ${v} as TreatmentRichContent,\n`;
out += '}\n';

fs.writeFileSync(path.join(dir, 'index.ts'), out);
console.log(`wrote lib/data/treatment-content/index.ts with ${files.length} sidecars`);
